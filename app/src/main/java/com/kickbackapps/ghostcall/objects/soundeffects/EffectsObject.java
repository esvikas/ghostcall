package com.kickbackapps.ghostcall.objects.soundeffects;

/**
 * Created by Ynott on 8/11/15.
 */
public class EffectsObject {

    String effectsName;
    String effectsURL;
    String effectsID;
    String effectsState;

    public String getEffectsName() {
        return effectsName;
    }

    public void setEffectsName(String effectsName) {
        this.effectsName = effectsName;
    }

    public String getEffectsURL() {
        return effectsURL;
    }

    public void setEffectsURL(String effectsURL) {
        this.effectsURL = effectsURL;
    }

    public String getEffectsID() {
        return effectsID;
    }

    public void setEffectsID(String effectsID) {
        this.effectsID = effectsID;
    }

    public String getEffectsState() {
        return effectsState;
    }

    public void setEffectsState(String effectsState) {
        this.effectsState = effectsState;
    }
}
